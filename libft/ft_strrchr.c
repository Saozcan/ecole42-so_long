#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*fp;

	c %= 256;
	fp = s;
	while (*s)
		s++;
	while (*s != *fp && *s != c)
		s--;
	if (*s == c)
		return ((char *)s);
	return (0);
}
